package pe.disenio.biblioteca.model;

public class Revistas {
	private String idRevista;
	private String titulo;
	private String idArticulo;
	private String idCategoria;
	private String tituloabrev;
	private String area_tematica;
	private String editor;
	private String ciudad;
	private String email;
	private String lugarEdicion;
	
	public String getIdRevista() {
		return idRevista;
	}
	public void setIdRevista(String idRevista) {
		this.idRevista = idRevista;
	}
	public String getTitulo() {
		return titulo;
	}
	public void setTitulo(String titulo) {
		this.titulo = titulo;
	}
	public String getIdArticulo() {
		return idArticulo;
	}
	public void setIdArticulo(String idArticulo) {
		this.idArticulo = idArticulo;
	}
	public String getIdCategoria() {
		return idCategoria;
	}
	public void setIdCategoria(String idCategoria) {
		this.idCategoria = idCategoria;
	}
	public String getTituloabrev() {
		return tituloabrev;
	}
	public void setTituloabrev(String tituloabrev) {
		this.tituloabrev = tituloabrev;
	}
	public String getArea_tematica() {
		return area_tematica;
	}
	public void setArea_tematica(String area_tematica) {
		this.area_tematica = area_tematica;
	}
	public String getEditor() {
		return editor;
	}
	public void setEditor(String editor) {
		this.editor = editor;
	}
	public String getCiudad() {
		return ciudad;
	}
	public void setCiudad(String ciudad) {
		this.ciudad = ciudad;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getLugarEdicion() {
		return lugarEdicion;
	}
	public void setLugarEdicion(String lugarEdicion) {
		this.lugarEdicion = lugarEdicion;
	}
	
	
}
